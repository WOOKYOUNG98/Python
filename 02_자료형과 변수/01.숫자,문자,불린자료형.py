# - 숫자 자료형
# 1. 정수형 : 소수점이 없는 수 
# sep='' 띄어쓰리를 없애고 싶을때
print(1, 3, 0, -1, sep="")

# 2. 실수형 : 소수점이 있는 수
print(1.1, 3.1, 0, -1.4)

# - 문자열, 자료형
# "" or ''
# end='' 한줄에 나오려면 사용한다
print("파이썬 재밌다", end='')
print('나 꼭 취업 성공할거야! 포기하지 말자!')
print('"안녕"이라고 말했다')

# - 불린형 자료형
# True or False
print(True)
print(False)


